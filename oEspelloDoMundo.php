    <h2 class="text-center" style="font-size:4em">O espello do mundo</h2>
    <p>Martiño recibe o encargo de desentrañar os contidos duns documentos de moita antigüidade que teñen que ver co devir histórico dun convento de monxas denominadas «onas». Neles emerxe Ona Guiomar, a abadesa de San Pedro de Ramirás, que mantén unha enigmática correspondencia co estranxeiro.</p>
    <p class="more">O espello do mundo, primeira novela de Ramón Nicolás, presenta un feitío de novela histórica, incorporando textos de orixe documental, que por veces son pura fabulación e outras non, ao servizo do deseño dunha época esvaecida na historia de Galicia que cobra, agora, unha nova perspectiva. A conexión coa trama histórica, enraizada no cenobio feminino de San Pedro de Ramirás (Ourense), vincúlase, ideolóxica e espiritualmente, con determinadas correntes relixiosas da Alemaña da época e particularmente coa figura de Hildegarda von Bingen. A esta realidade asómase Martiño, quen deberá afondar na revelación dos segredos que se atesouran nunha relación epistolar inédita con contidos altamente reveladores.</p>
